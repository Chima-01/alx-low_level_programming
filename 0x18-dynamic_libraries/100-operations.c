#include <stdio.h>

/**
 * Thie functions below is an arithmetic library
 * explicitly created
 * The functions below are to be used in a python script
 */

int add(int a, int b)
{
	return a + b;
}

int sub(int a, int b)
{
	return a - b;
}

int mul(int a, int b)
{
	return a * b;
}

int div(int a, int b)
{
	return a / b;
}

int mod(int a, int b)
{
	return a % b;
}
